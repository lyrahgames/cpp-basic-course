#include <iostream>

int main() {
  int x = 10;
  int y = 8;
  std::cout << "x = " << x << "\ny = " << y << std::endl;
  int a = x + y;
  std::cout << "a = " << a << std::endl;
  int b = x - y;
  std::cout << "b = " << b << std::endl;
  int c = x * y;
  std::cout << "c = " << c << std::endl;
  int d = x / y;
  std::cout << "d = " << d << std::endl;
  int e = x % y;
  std::cout << "e = " << e << std::endl;
}